		<h1 class="chd-game-play-main-head">Gameplay</h1>
		<p>The gameplay is similar to the original Stronghold, the major difference being that the game is set in the Middle East. As a consequence, farms can only be built on oasis grass, which leads to rivalry among players for limited farmland and resources. The game adds new AI opponents (the number depending on the version of the game) and several new Arabian units purchasable from a mercenary post. The colour of the player's units have also been changed from blue to red in order to match the colours of the Knights Templar. Other than farms there are other resources such as iron ore, quarry (for stone), marshes (for oil). These resources are deposited on the stockpile and player can choose to either sell or use them for defense purposes. There are two ways to accumulate army; either make the weapons and then spend a little gold to turn peasants into army men, or turn them directly into soldiers using more gold via the mercenary post.</p>
		<p>There are historical chapters (mostly fictionalized) which are to be completed using the resources given to the player at the start of the missions. Along with that there are 50 levels designed with increasing difficulty. Each level has one or more Kings who you have to defeat and defend your kingdom from. There is an option of 3 chickens at the start of the game which the player can use to skip a particular level.</p>
		<h1 class="chd-game-play-main-char">Characters</h1>
		<p>The game contains several different characters, available to be selected as allies and enemies. The opponents from the original game make a return, as well as new Arabian lords and crusaders being available. One unique aspect of the game is that the characters have individual binks (small short videos) by which they communicate with the player, asking for goods or help if allied with them or, if they are the players' enemy, taunting them when attacking or expressing worry when under siege. These videos were absent for Stronghold 2 but returned, in new form for Stronghold Crusader II.</p>
		<p>There are 16 characters in total. These vary in terms of difficulty and tactics. There are five relatively weak European lords, the Rat, the Snake, King Phillip, the Marshal and the Abbot. The Sheriff and Pig are of medium difficulty and of hard difficulty are the Lionheart, the Wolf, and Emperor Frederick. For the Arabian lords, the Sultan is of easy difficulty. The Caliph and the Emir are of medium difficulty and of hard difficulty are Nizar, the Wazir in real story Hassan-i Sabbah , and Saladin.</p>
